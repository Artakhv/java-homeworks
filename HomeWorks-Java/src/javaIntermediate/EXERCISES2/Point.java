package javaIntermediate.EXERCISES2;

public class Point {
    final private double x;
    final private double y;

    public Point(final double x, final double y) {
        this.x = x;
        this.y = y;
    }

    public final double getX() {
        return x;
    }

    public final double getY() {
        return y;
    }
}
